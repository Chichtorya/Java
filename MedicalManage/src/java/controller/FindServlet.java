    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        UserDAO dao = new UserDAO();
        String name = request.getParameter("name");
        if (name == null) {
            name = "";
        }
        String role = request.getParameter("role");
        try {
            int role1 = Integer.parseInt(role);
            request.setAttribute("role1", role);
            request.setAttribute("name1", name);
             ArrayList<User> list = new ArrayList<>();
            if(role1 != 0){
             list = dao.getAllUserByNameAndRole(1,name, role1);
            }else{
              list = dao.getAllUserByName(1,name);   
            }
            request.setAttribute("ListAcc", list);
            request.getRequestDispatcher("ListAccount.jsp").forward(request, response);
        } catch (NumberFormatException e) {
            response.sendRedirect("/login");
        }

    }

